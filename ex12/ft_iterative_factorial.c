int		ft_iterative_factorial(int nb)
{
	int i;
	int t;

	t = 1;
	i = 1;
	if (nb > 12 || nb < 0)
		return (0);
	while (i <= nb)
	{
		t = i * t;
		i++;
	}
	return (t);
}
